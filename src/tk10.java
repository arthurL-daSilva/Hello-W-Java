public class tk10 {
    public static void main(String[] args) {
        
        String dia = "sexta";
        
        switch (dia) {
            case "domingo":
                System.out.println("é domingo!!");
                break;
            case "segunda":
                System.out.println("é segunda!!");
                break;
            case "terça":
                System.out.println("é terça!!");
                break;
            default:
            System.out.println("Não é um dia.");
        }
    }
}
